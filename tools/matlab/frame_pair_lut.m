function display_pairs(a)
for i = 1:length(a)
    b = a{i} - 1;
    fprintf('{');
    for j = 1:size(b, 1)
        fprintf('{%d, %d}', b(j, 1), b(j, 2));
        if j ~= size(b, 1)
            fprintf(', ');
        end
    end
    fprintf('},\n');
end
